# Урок 2. Строки — работа с текстом.
# Запусти командой:  python lessons/01_basics/strings.py

# --- 1. Создаём строку ---
sentence = "Суд установил, что ответчик нарушил условия договора."

print("Исходный текст:")
print(sentence)

# --- 2. Длина строки (количество символов) ---
print("\nДлина текста:", len(sentence), "символов")

# --- 3. Верхний и нижний регистр ---
print("\nВсё заглавными:", sentence.upper())
print("Всё строчными: ", sentence.lower())

# --- 4. Поиск слова в тексте ---
keyword = "договора"

if keyword in sentence:
    print("\nСлово '{}' найдено в тексте.".format(keyword))
else:
    print("\nСлово '{}' не найдено.".format(keyword))

# --- 5. Сколько раз слово встречается ---
text = "Истец подал иск. Истец требует возмещения. Истец явился в суд."
word = "Истец"
count = text.count(word)
print("\nСколько раз встречается '{}': {}".format(word, count))

